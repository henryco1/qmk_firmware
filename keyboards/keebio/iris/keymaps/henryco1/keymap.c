//========================================
// Keymap Main Handler
//========================================

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case COLEMAK:
      if (record->event.pressed) {
        layer_on(_COLEMAK);
      } else {
        layer_off(_COLEMAK);
      }
      return false;
      break;
    case NAVIGATE:
      if (record->event.pressed) {
        layer_on(_NAVIGATE);
      } else {
        layer_off(_NAVIGATE);
      }
      return false;
      break;
    case NUMPAD:
      if (record->event.pressed) {
        layer_on(_NUMPAD);
      } else {
        layer_off(_NUMPAD);
      }
      return false;
      break;      
    case SYMBOL_LEFT:
      if (record->event.pressed) {
        layer_on(_SYMBOL_LEFT);
      } else {
        layer_off(_SYMBOL_LEFT);
      }
      return false;
      break;
    case SYMBOL_RIGHT:
      if (record->event.pressed) {
        layer_on(_SYMBOL_RIGHT);
      } else {
        layer_off(_SYMBOL_RIGHT);
      }
      return false;
      break;
    case FUNCTION:
      if (record->event.pressed) {
        layer_on(_FUNCTION);
      } else {
        layer_off(_FUNCTION);
      }
      return false;
      break;
  }
  return true;
}
