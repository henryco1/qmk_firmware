bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case NUMPAD:
      if (record->event.pressed) {
        layer_on(_NUMPAD);
        update_tri_layer(_NUMPAD, _SYMBOL, _HOMEMOD);
      } else {
        layer_off(_NUMPAD);
        update_tri_layer(_NUMPAD, _SYMBOL, _HOMEMOD);
      }
      return false;
      break;
    case SYMBOL:
      if (record->event.pressed) {
        layer_on(_SYMBOL);
        update_tri_layer(_NUMPAD, _SYMBOL, _HOMEMOD);
      } else {
        layer_off(_SYMBOL);
        update_tri_layer(_NUMPAD, _SYMBOL, _HOMEMOD);
      }
      return false;
      break;
    case HOMEMOD:
      if (record->event.pressed) {
        layer_on(_HOMEMOD);
      } else {
        layer_off(_HOMEMOD);
      }
      return false;
      break;
  }
  return true;
}
